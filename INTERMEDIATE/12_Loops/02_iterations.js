// WHILE LOOP

let index = 0
while (index <= 10) {
    // console.log(`Value of Index is ${index}`);
    index+=2; // OR WE CAN WRITE IT AS {index = index + 2}
}

let myArray = ["flash", "batman", "superman"]
let arr = 0;

while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr+=1;
}


// DO WHILE LOOP

let score = 1;

do {
    // console.log(`Score is ${score}`);
    score++;
} while (score <= 10);


